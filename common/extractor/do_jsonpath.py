from jsonpath import jsonpath
from common.tools.logger import MyLog


def do_json(response, expression, index=None):
    """

    Args:
        response:
        expression:
        index:

    Returns:

    """
    if index is None or index == "":
        result = []
        try:
            for each in jsonpath(response, expression):
                result.append(str(each))
            return result
        except Exception as e:
            MyLog().my_log("expression:{}".format(expression))
            raise e
    elif index == "0":
        try:
            result = jsonpath(response, expression)[0]
            return str(len(result))
        except Exception as e:
            MyLog().my_log("expression:{}".format(expression))
            raise e
    elif index == "1":
        try:
            result = jsonpath(response, expression)
            if result is False:
                return "0"
            else:
                return str(len(result))
        except Exception as e:
            MyLog().my_log("expression:{}".format(expression))
            raise e
    elif index == "2":
        try:
            result = len(response)
            return str(result)
        except Exception as e:
            MyLog().my_log("response:{}".format(response))
            raise e
    else:
        try:
            result = jsonpath(response, expression)[index]
            return str(result)
        except Exception as e:
            MyLog().my_log("expression:{}".format(expression))
            raise e


def get_data(res, j_ex):
    """

    Args:
        res:响应结果
        j_ex: jsonpath 表达式，若字符串，则返回具体结果列表，若列表，则返回提取结果列表的长度

    Returns:

    """
    expect = {}
    for k, v in j_ex.items():
        if isinstance(v, str):
            expect[k] = jsonpath(res, v)
        elif isinstance(v, list):
            if len(v) <= 1:
                # 获取长度
                expect[k] = [len(jsonpath(res, v[0]))]
            else:
                expect[k] = jsonpath(res, v[0])[v[1]]
    return expect


